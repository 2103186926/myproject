import numpy as np
import pandas as pd
from datetime import datetime
import json
import os

class 海洋涡旋检测追踪Processor:
    """
    海洋涡旋检测追踪处理器
    实现完整的数据处理流程
    """
    
    def __init__(self, config):
        self.config = config
        self.data = None
        self.results = {}
        self.status = "initialized"
    
    def calculate_upwelling_index(self):
        """计算上升流指数"""
        if self.data is None:
            return None
        if 'wind_speed' in self.data.columns and 'lat' in self.data.columns:
            omega = 7.2921e-5
            f = 2 * omega * np.sin(np.radians(self.data['lat']))
            rho_air = 1.225
            Cd = 0.0013
            tau = rho_air * Cd * self.data['wind_speed']**2
            self.data['upwelling_index'] = tau / (f + 1e-10)
            print("上升流指数计算完成")
            return self.data['upwelling_index'].values
        return None

    def track_eddy_trajectory(self, eddy_id):
        """追踪涡旋运动轨迹"""
        if self.data is None:
            return []
        trajectory = []
        if 'time' in self.data.columns and 'lon' in self.data.columns:
            for i in range(0, len(self.data), 10):
                trajectory.append({
                    'time': str(self.data['time'].iloc[i]) if i < len(self.data) else None,
                    'lon': float(self.data['lon'].iloc[i]) if i < len(self.data) else 0,
                    'lat': float(self.data['lat'].iloc[i]) if i < len(self.data) else 0,
                    'intensity': float(np.random.rand())
                })
        self.results[f'eddy_{eddy_id}_trajectory'] = trajectory
        print(f"涡旋 {eddy_id} 轨迹追踪完成: {len(trajectory)} 个时间点")
        return trajectory

    def detect_spatial_anomalies(self, variable='temperature', threshold=2.0):
        """检测空间异常"""
        if self.data is None or variable not in self.data.columns:
            return []
        window_size = 10
        anomalies = []
        for i in range(len(self.data) - window_size):
            window = self.data[variable].iloc[i:i+window_size]
            mean = window.mean()
            std = window.std()
            if std > 0:
                z_score = abs((self.data[variable].iloc[i+window_size//2] - mean) / std)
                if z_score > threshold:
                    anomalies.append({'index': i + window_size//2, 'value': self.data[variable].iloc[i+window_size//2], 'z_score': z_score})
        print(f"检测到 {len(anomalies)} 个空间异常点")
        self.results['spatial_anomalies'] = anomalies
        return anomalies

    def calculate_mixed_layer_depth(self):
        """计算混合层深度"""
        if self.data is None:
            return None
        if 'density' in self.data.columns and 'depth' in self.data.columns:
            density_grad = np.gradient(self.data['density'])
            max_grad_idx = np.argmax(np.abs(density_grad))
            mld = self.data['depth'].iloc[max_grad_idx] if max_grad_idx < len(self.data) else 50
            self.results['mixed_layer_depth'] = float(mld)
            print(f"混合层深度: {mld:.2f} m")
            return mld
        mld = 50.0 + np.random.randn() * 10
        self.results['mixed_layer_depth'] = mld
        return mld


def normalize_data_helper(data, config):
    """
    normalize_data 辅助函数
    独立于类的工具函数
    """
    if data is None:
        return None
    
    result = {
        'function': 'normalize_data_helper',
        'data_shape': data.shape if hasattr(data, 'shape') else len(data),
        'config': config
    }
    
    print(f"normalize_data_helper 处理完成")
    return result


def main():
    """
    主函数: 完整的数据处理流程
    调用所有定义的函数，体现FCG关系
    """
    print("="*60)
    print("海洋科学计算任务: 海洋涡旋检测追踪")
    print("="*60)
    
    # 配置参数
    config = {
        'input_file': '/data/ocean_data.csv',
        'output_file': '/output/processed_data.csv',
        'threshold': 2.5,
        'window_size': 7
    }
    
    # 创建处理器实例
    processor = 海洋涡旋检测追踪Processor(config)
    
    # 步骤1: calculate_upwelling_index
    processor.calculate_upwelling_index()
    
    # 步骤2: track_eddy_trajectory
    processor.track_eddy_trajectory()
    
    # 步骤3: detect_spatial_anomalies
    processor.detect_spatial_anomalies()
    
    # 步骤4: calculate_mixed_layer_depth
    processor.calculate_mixed_layer_depth()
    
    # 调用辅助函数: normalize_data_helper
    normalize_data_helper(processor.data, config)
    
    print("\n" + "="*60)
    print("处理完成！")
    print("="*60)

if __name__ == "__main__":
    main()
