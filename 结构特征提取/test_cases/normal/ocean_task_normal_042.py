def main():
    """
    主函数: 完整的数据处理流程
    调用所有定义的函数，体现FCG关系
    """
    print("="*60)
    print("海洋科学云计算任务执行")
    print("="*60)
    
    # 配置参数
    config = {
        'input_file': '/data/ocean_data.csv',
        'output_file': '/output/processed_data.csv',
        'threshold': 2.5,
        'window_size': 7
    }
    
    # 创建处理器实例
    processor = ADCP流速分析Processor(config)
    
    # 步骤1: visualize_data
    if not processor.visualize_data(config['input_file']):
        print("处理失败，退出")
        return
    
    # 步骤2: calculate_statistics
    processor.calculate_statistics()
    
    # 步骤3: apply_filter
    processor.apply_filter()
    
    # 步骤4: preprocess_data
    processor.preprocess_data()
    
    # 步骤5: classify_patterns
    processor.classify_patterns()
    
    # 步骤6: load_data
    processor.load_data()
    
    # 步骤7: generate_report
    processor.generate_report()
    
    # 步骤8: find_extrema
